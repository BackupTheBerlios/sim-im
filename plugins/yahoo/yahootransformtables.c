int   yahoo_M1Common(unsigned b, int Parameter)
{
    unsigned d, a;
    unsigned c = (b & 0xFF) * 0x9E3779B1;

    c ^= (b >> 8) & 0xFF;
    c *= 0x9E3779B1;
    c ^= (b >> 16) & 0xFF;
    c *= 0x9E3779B1;
    c ^= b >> 24;
    c *= 0x9E3779B1;

    if(Parameter <= 1) return b;

    b *= 0x10DCD;

    d = c >> 8;
    a = c ^ d;
    a = a >> 16;
    a = (a ^ c ^ d) & 0xFF;

    return (yahoo_M1[(unsigned) (a % 96)])((int)b, Parameter-1);
}

